    val a = 10
    val b = 20
    addtion(a,b)
    subtraction(a,b)
    multiplication(a,b)
    divison(a,b)
}
fun addtion(a:Int,b:Int){
    var c = a+b;
    println("The Addition Is $c")
}
fun subtraction(a:Int,b:Int){
    var s = a-b;
    println("The Subtraction Is $s")
}
fun multiplication(a:Int,b:Int){
    var m = a+b;
    println("The Multiplication Is $m")
}
fun divison(a:Int,b:Int){
    var d = a//b;
    println("The Divison Is $d")
}
